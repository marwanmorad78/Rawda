def get_language(request):
    lang = request.session.get("site_language", DEFAULT_LANGUAGE)
    return lang if lang in LANGUAGES else DEFAULT_LANGUAGE


def get_ui_strings(language):
    return UI_STRINGS.get(language, UI_STRINGS[DEFAULT_LANGUAGE])


def localize_instance(instance, language, fields):
    if instance is None:
        return None
    for field in fields:
        localized_value = getattr(instance, f"{field}_ar", "") if language == "ar" else ""
        setattr(instance, f"display_{field}", localized_value or getattr(instance, field, ""))
    return instance


def localize_queryset(instances, language, fields):
    return [localize_instance(instance, language, fields) for instance in instances]


def with_lang_query(path, language):
    query = urlencode({"lang": language})
    return f"{path}?{query}"


def _format_amount(value):
    amount = Decimal(value)
    if amount == amount.quantize(Decimal("1")):
        return f"{int(amount):,}"
    return f"{amount:,.2f}"


def format_syp(value, language="en"):
    formatted = _format_amount(value)
    code = UI_STRINGS.get(language, UI_STRINGS[DEFAULT_LANGUAGE])["currency_code"]
    return f"{formatted} {code}"


def format_price_range(min_value, max_value, language="en"):
    min_amount = Decimal(min_value)
    max_amount = Decimal(max_value)
    if min_amount == max_amount:
        return format_syp(min_amount, language)
    code = UI_STRINGS.get(language, UI_STRINGS[DEFAULT_LANGUAGE])["currency_code"]
    return f"{_format_amount(min_amount)} ~ {_format_amount(max_amount)} {code}"
